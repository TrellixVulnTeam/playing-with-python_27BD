# Make function called subtraction
def subtraction(x,y):
    # Make subtract variable equal to x - y
    subtract = x - y

    # Return subtract variable
    return subtract

print (subtraction(10, 4))

def moreSubtraction(x,y, z):
    # Make subtract variable equal to x - y - z
    subtract = x - y - z

    # Return subtract variable
    return subtract

print (moreSubtraction(40, 3, 11))

def multiplication(x,y):
    # Make product variable equal to x * y
    product = x * y

    # Return product variable
    return product

def division(x,y):
    # Make quotient variable equal to x / y
    quotient = float(x) / float(y)

    # Return quotient variable
    return quotient


